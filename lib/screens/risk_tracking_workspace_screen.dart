  Color _getRiskScoreColor(int score) {
    if (score >= 15) return const Color(0xFFDC2626);
    if (score >= 10) return const Color(0xFFF97316);
    if (score >= 5) return const Color(0xFFF59E0B);
    return const Color(0xFF10B981);
  }

  Color _getScoreColor(int score) {
    if (score >= 4) return const Color(0xFFDC2626);
    if (score >= 3) return const Color(0xFFF97316);
    if (score >= 2) return const Color(0xFFF59E0B);
    return const Color(0xFF10B981);
  }

  Color _getProgressColor(int progress) {
    if (progress >= 80) return const Color(0xFF10B981);
    if (progress >= 50) return const Color(0xFF6366F1);
    if (progress >= 25) return const Color(0xFFF59E0B);
    return const Color(0xFFEF4444);
  }

  IconData _getSeverityIcon(String severity) {
    switch (severity.toLowerCase()) {
      case 'critical':
      case 'high':
        return Icons.error_rounded;
      case 'medium':
        return Icons.warning_rounded;
      case 'low':
        return Icons.info_rounded;
      default:
        return Icons.info_outline;
    }
  }

  String _formatDateShort(String dateStr) {
    if (dateStr == 'TBD' || dateStr.isEmpty) return 'TBD';
    try {
      final parts = dateStr.split('-');
      if (parts.length == 3) {
        return '${parts[1]}/${parts[2]}';
      }
    } catch (_) {}
    return dateStr;
  }

