                            onChanged: (v) =>
                                setDialogState(() => status = v ?? status),
                            decoration: _inputDecoration(
                                'Status', Icons.check_circle_outline),
                          ),
                        ),
                      ],
                    ),
                    const SizedBox(height: 14),
                    Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Row(
                          children: [
                            const Text('Progress: ',
                                style: TextStyle(
                                    fontSize: 13, fontWeight: FontWeight.w600)),
                            Text('$progress%',
                                style: TextStyle(
                                    fontSize: 13,
                                    fontWeight: FontWeight.w700,
                                    color: _getProgressColor(progress))),
                          ],
                        ),
                        const SizedBox(height: 8),
                        Slider(
                          value: progress.toDouble(),
                          min: 0,
                          max: 100,
                          divisions: 20,
                          activeColor: _getProgressColor(progress),
                          onChanged: (v) =>
                              setDialogState(() => progress = v.round()),
                        ),
                      ],
                    ),
                    const SizedBox(height: 14),
                    TextFormField(
                      controller: statusNotesController,
                      maxLines: 2,
                      decoration:
                          _inputDecoration('Status Notes', Icons.note_outlined),
                      textCapitalization: TextCapitalization.sentences,
                    ),
                    const SizedBox(height: 14),
                    DropdownButtonFormField<String>(
                      initialValue: associatedRiskIdController.text.isEmpty
                          ? null
                          : associatedRiskIdController.text,
                      items: _risks
                          .map((r) => DropdownMenuItem(
                                value: r.id,
                                child: Text(r.title,
                                    maxLines: 1,
                                    overflow: TextOverflow.ellipsis),
                              ))
                          .toList(),
                      onChanged: (v) => setDialogState(
                          () => associatedRiskIdController.text = v ?? ''),
                      decoration:
                          _inputDecoration('Associated Risk', Icons.link),
                      hint: const Text('Link to a risk'),
                    ),
                    const SizedBox(height: 24),
                    Row(
                      mainAxisAlignment: MainAxisAlignment.end,
                      children: [
                        TextButton(
                          onPressed: () => Navigator.pop(context, false),
                          style: TextButton.styleFrom(
                            padding: const EdgeInsets.symmetric(
                                horizontal: 24, vertical: 14),
                            shape: RoundedRectangleBorder(
                                borderRadius: BorderRadius.circular(10)),
                          ),
                          child: const Text('Cancel',
                              style: TextStyle(fontWeight: FontWeight.w600)),
                        ),
                        const SizedBox(width: 12),
                        FilledButton(
                          onPressed: () {
                            if (!(formKey.currentState?.validate() ?? false)) {
                              return;
                            }
                            Navigator.pop(context, true);
                          },
                          style: FilledButton.styleFrom(
                            backgroundColor: const Color(0xFF6366F1),
                            padding: const EdgeInsets.symmetric(
                                horizontal: 28, vertical: 14),
                            shape: RoundedRectangleBorder(
                                borderRadius: BorderRadius.circular(10)),
                          ),
                          child: Text(
                              existing == null ? 'Add Plan' : 'Save Changes',
                              style:
                                  const TextStyle(fontWeight: FontWeight.w600)),
                        ),
                      ],
                    ),
                  ],
                ),
              ),
            ),
          ),
        ),
      ),
    );

    titleController.dispose();
    descriptionController.dispose();
    ownerController.dispose();
    dueController.dispose();
    estimatedCostController.dispose();
    statusNotesController.dispose();
    associatedRiskIdController.dispose();

    if (result != true || !mounted) return;

    final newMitigation = _MitigationData(
      id: existing?.id ?? DateTime.now().microsecondsSinceEpoch.toString(),
      title: titleController.text.trim(),
      description: descriptionController.text.trim(),
      owner: ownerController.text.trim().isEmpty
          ? 'Unassigned'
          : ownerController.text.trim(),
      status: status,
      dueDate:
          dueController.text.trim().isEmpty ? 'TBD' : dueController.text.trim(),
      progress: progress,
      estimatedCost: estimatedCostController.text.trim(),
      statusNotes: statusNotesController.text.trim(),
      associatedRiskId: associatedRiskIdController.text.trim(),
      createdAt: existing?.createdAt ?? _formatDate(DateTime.now()),
    );

    setState(() {
      final index = _mitigations.indexWhere((m) => m.id == newMitigation.id);
      if (index == -1) {
        _mitigations.insert(0, newMitigation);
      } else {
        _mitigations[index] = newMitigation;
      }
    });
    _saveToFirestore();
  }

  InputDecoration _inputDecoration(String label, IconData icon) {
    return InputDecoration(
      labelText: label,
      prefixIcon: Icon(icon, size: 20),
      border: OutlineInputBorder(
        borderRadius: BorderRadius.circular(12),
        borderSide: const BorderSide(color: Color(0xFFD1D5DB)),
      ),
      enabledBorder: OutlineInputBorder(
        borderRadius: BorderRadius.circular(12),
        borderSide: const BorderSide(color: Color(0xFFD1D5DB)),
      ),
      focusedBorder: OutlineInputBorder(
        borderRadius: BorderRadius.circular(12),
        borderSide: const BorderSide(color: Color(0xFF6366F1), width: 2),
      ),
      contentPadding: const EdgeInsets.symmetric(horizontal: 14, vertical: 14),
      filled: true,
      fillColor: Colors.white,
    );
  }

  void _deleteRisk(String id) {
    setState(() => _risks.removeWhere((r) => r.id == id));
    _saveToFirestore();
  }

  void _deleteSignal(String id) {
    setState(() => _signals.removeWhere((s) => s.id == id));
    _saveToFirestore();
  }

  void _deleteMitigation(String id) {
    setState(() => _mitigations.removeWhere((m) => m.id == id));
    _saveToFirestore();
  }
}

