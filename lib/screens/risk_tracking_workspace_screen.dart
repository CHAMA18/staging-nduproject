  String _formatDate(DateTime date) {
    return '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
  }

  void _syncSharedProjectData() {
    final provider = ProjectDataInherited.maybeRead(context);
    if (provider == null) return;
    final riskTitlesById = {
      for (final risk in _risks)
        if (risk.id.trim().isNotEmpty) risk.id.trim(): risk.title.trim(),
    };
    provider.updateField(
      (data) => data.copyWith(
        executionRiskItems:
            _risks.map((risk) => risk.toShared()).toList(growable: false),
        executionRiskSignals:
            _signals.map((signal) => signal.toShared()).toList(growable: false),
        executionRiskMitigations: _mitigations
            .map((mitigation) => mitigation.toShared(
                  associatedRiskTitle:
                      riskTitlesById[mitigation.associatedRiskId.trim()] ?? '',
                ))
            .toList(growable: false),
      ),
    );
  }

  void _saveToFirestore() {
    _syncSharedProjectData();
    final projectId = _projectId;
    if (projectId == null || projectId.isEmpty) return;

    _saveDebounce?.cancel();
    _saveDebounce = Timer(const Duration(milliseconds: 600), () async {
      try {
        final provider = ProjectDataInherited.maybeRead(context);
        await provider?.saveToFirebase(checkpoint: 'risk_tracking');
        await FirebaseFirestore.instance
            .collection('projects')
            .doc(projectId)
            .collection('execution_phase_sections')
            .doc('risk_tracking')
            .set({
          'riskItems': _risks.map((r) => r.toMap()).toList(),
          'riskSignals': _signals.map((s) => s.toMap()).toList(),
          'mitigationPlans': _mitigations.map((m) => m.toMap()).toList(),
          'updatedAt': FieldValue.serverTimestamp(),
          'updatedBy': _userId,
        }, SetOptions(merge: true));
      } catch (e) {
        debugPrint('Error saving risk tracking: $e');
      }
    });
  }

  List<_RiskData> get _filteredRisks {
    if (_selectedFilters.contains('All risks')) return _risks;
    return _risks.where((risk) {
      if (_selectedFilters.contains('High impact')) {
        return risk.impact == 'High' || risk.impact == 'Critical';
      }
      if (_selectedFilters.contains('Needs review')) {
        return risk.status == 'Open' || risk.status == 'At Risk';
      }
      if (_selectedFilters.contains('Blocked')) return risk.status == 'Blocked';
      if (_selectedFilters.contains('Critical risks')) {
        return risk.riskScore >= 15;
      }
      return true;
    }).toList();
  }

