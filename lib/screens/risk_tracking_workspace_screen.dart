  @override
  void initState() {
    super.initState();
    _loadData();
  }

  @override
  void dispose() {
    _saveDebounce?.cancel();
    super.dispose();
  }

  Future<void> _loadData() async {
    final sharedProjectData =
        ProjectDataInherited.maybeRead(context)?.projectData;
    if (sharedProjectData != null &&
        (sharedProjectData.executionRiskItems.isNotEmpty ||
            sharedProjectData.executionRiskSignals.isNotEmpty ||
            sharedProjectData.executionRiskMitigations.isNotEmpty)) {
      setState(() {
        _risks
          ..clear()
          ..addAll(
              sharedProjectData.executionRiskItems.map(_RiskData.fromShared));
        _signals
          ..clear()
          ..addAll(sharedProjectData.executionRiskSignals
              .map(_SignalData.fromShared));
        _mitigations
          ..clear()
          ..addAll(sharedProjectData.executionRiskMitigations
              .map(_MitigationData.fromShared));
      });
      await _autoGenerateIfNeeded();
      return;
    }

    final projectId = _projectId;
    if (projectId == null || projectId.isEmpty) {
      return;
    }

    try {
      final doc = await FirebaseFirestore.instance
          .collection('projects')
          .doc(projectId)
          .collection('execution_phase_sections')
          .doc('risk_tracking')
          .get();

      if (!mounted) return;

      final data = doc.data();
      if (data != null) {
        setState(() {
          _risks.clear();
          _risks.addAll(((data['riskItems'] as List?) ?? const []).map(
              (item) => _RiskData.fromMap(Map<String, dynamic>.from(item))));

          _signals.clear();
          _signals.addAll(((data['riskSignals'] as List?) ?? const []).map(
              (item) => _SignalData.fromMap(Map<String, dynamic>.from(item))));

          _mitigations.clear();
          _mitigations.addAll(((data['mitigationPlans'] as List?) ?? const [])
              .map((item) =>
                  _MitigationData.fromMap(Map<String, dynamic>.from(item))));
        });
        _syncSharedProjectData();
        await ProjectDataInherited.maybeRead(context)
            ?.saveToFirebase(checkpoint: 'risk_tracking');
      }

      await _autoGenerateIfNeeded();
    } catch (e) {
      debugPrint('Error loading risk tracking: $e');
    }
  }

  Future<void> _autoGenerateIfNeeded() async {
    if (_autoGenerated || _isGenerating) return;
